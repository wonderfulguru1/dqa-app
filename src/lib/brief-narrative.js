export function buildBriefNarrative({
  stateLabel,
  period,
  today,
  facilitiesCount,
  txCount,
  retrieval,
  avgTxConc,
  qualityLevel,
  aggCount,
  aggAccurateRate,
  aggAccurate,
  foldersFound,
  indSummary,
  annotatedIssues,
  openIssues,
  overdueIssues,
  discrepancyCount,
}) {
  const parts = [
    'ECEWS Data Quality Assessment. State Out-Brief.',
    `State: ${stateLabel}. Period: ${period}. Generated ${today}.`,
    'Section 1. Executive Summary.',
    `The ECEWS DQA team conducted a data quality assessment across ${facilitiesCount} ${facilitiesCount === 1 ? 'facility' : 'facilities'} in ${stateLabel} for the ${period} reporting period.`,
    `A total of ${txCount} client-level records were reviewed for TX_NEW, with a folder retrieval rate of ${retrieval != null ? retrieval + ' percent' : 'not available'}.`,
    `The overall client-level concurrence was ${avgTxConc != null ? avgTxConc + ' percent' : 'insufficient data'}, indicating ${qualityLevel}.`,
    `For aggregate indicators, ${aggCount} indicator-facility pairs were validated with an overall accurate reporting rate of ${aggAccurateRate != null ? aggAccurateRate + ' percent' : 'insufficient data'}.`,
    'Section 2. TX_NEW Client-Level Findings.',
    `Clients sampled: ${txCount}. Folders retrieved: ${foldersFound}${retrieval != null ? `, a ${retrieval} percent retrieval rate` : ''}.`,
    `Average concurrence: ${avgTxConc != null ? avgTxConc + ' percent' : 'not available'}. Facilities visited: ${facilitiesCount}.`,
    'Section 3. Aggregate Indicator Concurrence.',
    aggCount > 0
      ? `${aggCount} aggregate indicator rows were validated. ${aggAccurateRate != null ? `${aggAccurateRate} percent, ${aggAccurate} of ${aggCount}, were accurately reported.` : 'Accurate reporting rate not available.'}`
      : 'No aggregate data available.',
    discrepancyCount > 0
      ? `${discrepancyCount} under-reported or over-reported values were identified requiring follow-up.`
      : '',
  ]

  if (indSummary.length > 0) {
    parts.push('Indicator summary.')
    for (const row of indSummary) {
      const rate = row.total > 0 ? Math.round(row.accurate / row.total * 100) : null
      parts.push(
        `${row.indicator}: ${row.total} facilities assessed, ${row.accurate} accurately reported${rate != null ? `, ${rate} percent accurate reporting rate` : ''}.`,
      )
    }
  }

  parts.push('Section 4. Issues and Recommended Actions.')
  parts.push(`${annotatedIssues.length} issues were documented during the assessment.`)
  if (openIssues > 0) parts.push(`${openIssues} remain open.`)
  if (overdueIssues > 0) parts.push(`${overdueIssues} issue${overdueIssues === 1 ? ' is' : 's are'} past their due date.`)

  if (annotatedIssues.length > 0) {
    annotatedIssues.forEach((issue, idx) => {
      const lines = [
        `Issue ${idx + 1}.`,
        issue.facility ? `Facility: ${issue.facility}.` : '',
        issue.state ? `State: ${issue.state}.` : '',
        issue.thematicArea ? `Thematic area: ${issue.thematicArea}.` : '',
        issue.gap ? `Gap: ${issue.gap}.` : '',
        issue.proposedSolution ? `Recommended action: ${issue.proposedSolution}.` : '',
        issue.assessor ? `Assessor: ${issue.assessor}.` : '',
        issue.responsiblePerson ? `Responsible person: ${issue.responsiblePerson}.` : '',
        issue.dueDate ? `Due date: ${issue.dueDate}.` : '',
        issue._flag ? `Flag: ${issue._flag}.` : '',
        issue.status ? `Status: ${issue.status}.` : '',
      ].filter(Boolean)
      parts.push(lines.join(' '))
    })
  } else {
    parts.push('No mismatch resolutions or issues logged yet.')
  }

  parts.push(
    'Section 5. Conclusion.',
    `This out-brief was generated by the ECEWS DQA Companion system on ${today}.`,
    `Data reflects assessments conducted for ${period} in ${stateLabel}.`,
    'For detailed facility-level analysis, refer to the TX_NEW Client-Level and Aggregate Concurrence tabs.',
  )

  return parts.filter(Boolean).join(' ')
}
